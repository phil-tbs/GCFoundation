namespace GCFoundation.Components.Enums
{
    /// <summary>
    /// Defines the options for the autocomplete attribute on HTML form fields.
    /// This attribute controls whether the browser should automatically complete the input field based on previous entries.
    /// </summary>
    public enum AutocompleteType
    {
        /// <summary>
        /// Enables the browser's autocomplete feature for the input field, allowing previously entered values to be suggested.
        /// </summary>
        on,

        /// <summary>
        /// Disables the browser's autocomplete feature for the input field, preventing previous entries from being suggested.
        /// </summary>
        off
    }
}
