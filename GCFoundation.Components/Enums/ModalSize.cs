namespace GCFoundation.Components.Enums
{
    /// <summary>
    /// Defines available modal sizes for Modal.
    /// </summary>
    public enum ModalSize
    {
        /// <summary>
        /// Represents the default modal size. 
        /// </summary>
        Default,

        /// <summary>
        /// Represents a small modal size.
        /// This size is smaller than the default and is typically used for simple content or small forms.
        /// </summary>
        Small,

        /// <summary>
        /// Represents a large modal size.
        /// This size is larger than the default and is typically used for content that requires more space or more complex layouts.
        /// </summary>
        Large
    }
}
