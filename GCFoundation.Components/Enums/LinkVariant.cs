    /// <summary>
    /// Defines the available link variants for styling links.
    /// </summary>
    public enum LinkVariant
    {
        /// <summary>
        /// Represents the default link style.
        /// This is the standard link style, typically with an underline and a standard color.
        /// </summary>
        Default,

        /// <summary>
        /// Represents a lighter version of the link style.
        /// This variant typically applies a lighter color or a more subtle appearance compared to the default link style.
        /// </summary>
        Light
    }
}
