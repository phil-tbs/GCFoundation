namespace GCFoundation.Components.Enums
{
    /// <summary>
    /// Defines the available HTML tags for the title of a card component.
    /// This enum specifies which HTML tag can be used to render the card title, influencing its semantic meaning and styling.
    /// </summary>
    public enum CardTitleTag
    {
        /// <summary>
        /// The <c>&lt;a&gt;</c> tag, typically used for linking the card title to another page or resource.
        /// </summary>
        a,

        /// <summary>
        /// The <c>&lt;h3&gt;</c> tag, commonly used for a card title with a heading level of 3.
        /// </summary>
        h3,

        /// <summary>
        /// The <c>&lt;h4&gt;</c> tag, commonly used for a card title with a heading level of 4.
        /// </summary>
        h4,

        /// <summary>
        /// The <c>&lt;h5&gt;</c> tag, commonly used for a card title with a heading level of 5.
        /// </summary>
        h5,

        /// <summary>
        /// The <c>&lt;h6&gt;</c> tag, commonly used for a card title with a heading level of 6.
        /// </summary>
        h6
    }
}
