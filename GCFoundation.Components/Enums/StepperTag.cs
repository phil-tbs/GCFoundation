namespace GCFoundation.Components.Enums
{
    /// <summary>
    /// Defines the possible heading tags that can be used in the stepper component.
    /// </summary>
    public enum StepperTag
    {
        /// <summary>
        /// Represents an <c>h1</c> tag for the first-level heading.
        /// </summary>
        h1,

        /// <summary>
        /// Represents an <c>h2</c> tag for the second-level heading.
        /// </summary>
        h2,

        /// <summary>
        /// Represents an <c>h3</c> tag for the third-level heading.
        /// </summary>
        h3,
    }
}
