namespace Foundation.Components.Enums
{
    /// <summary>
    /// Defines the available text roles for styling text components.
    /// </summary>
    public enum TextRole
    {
        /// <summary>
        /// The primary text role, typically used for important text elements.
        /// </summary>
        Primary,

        /// <summary>
        /// The secondary text role, used for less important or secondary text elements.
        /// </summary>
        Secondary,

        /// <summary>
        /// The light text role, used for subtle or secondary text with lighter emphasis.
        /// </summary>
        Light
    }
}
