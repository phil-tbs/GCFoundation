using Foundation.Components.TagHelpers.GCDS;

namespace Foundation.Components.Enums
{
    /// <summary>
    /// Specifies the HTML tags that can be used with the <see cref="SrOnlyTagHelper"/> for visually hidden content that is still accessible to screen readers.
    /// </summary>
    public enum SrOnlyTag
    {
        /// <summary>
        /// Represents the <code>h1</code> HTML tag.
        /// </summary>
        h1,

        /// <summary>
        /// Represents the <code>h2</code> HTML tag.
        /// </summary>
        h2,

        /// <summary>
        /// Represents the <code>h3</code> HTML tag.
        /// </summary>
        h3,

        /// <summary>
        /// Represents the <code>h4</code> HTML tag.
        /// </summary>
        h4,

        /// <summary>
        /// Represents the <code>h5</code> HTML tag.
        /// </summary>
        h5,

        /// <summary>
        /// Represents the <code>h6</code> HTML tag.
        /// </summary>
        h6,

        /// <summary>
        /// Represents the <code>p</code> HTML tag (paragraph).
        /// </summary>
        p,

        /// <summary>
        /// Represents the <code>span</code> HTML tag (generic inline container).
        /// </summary>
        span
    }
}
